import webbrowser
import sys

def open_legal_databases(iin_bin):
    print(f"\n[+] Запуск легального сбора данных по ИИН/БИН: {iin_bin}")
    print("[*] Поиск выполняется исключительно по открытым государственным реестрам РК...")
    
    # Ссылки на официальные государственные порталы с подставленным номером для поиска
    urls = [
        # 1. Министерство юстиции РК — Реестр должников по исполнительным производствам (АИС ОИП)
        f"https://aisoip.adilet.gov.kz/public/debtors?iin={iin_bin}",
        
        # 2. Комитет государственных доходов (КГД) — Проверка налоговых задолженностей физлиц
        f"https://kgd.gov.kz/ru/app/tax-search-fl?iin={iin_bin}",
        
        # 3. Верховный Суд РК — Поиск дел и судебных актов в Судебном кабинете
        f"https://sud.gov.kz/rus/content/poisk-sudebnyh-del?search_iin={iin_bin}",
        
        # 4. Комитет госдоходов — Поиск налогоплательщиков (статус ТОО/ИП, признание лжепредприятием)
        f"https://kgd.gov.kz/ru/services/taxpayer_search/legal_entity?iin={iin_bin}"
    ]
    
    # Автоматически открываем эти реестры в твоем стандартном браузере
    for url in urls:
        webbrowser.open(url)
    
    print("\n[+] Готово! Все официальные вкладки открыты.")
    print("[!] Изучите результаты на страницах госорганов (может потребоваться ввод защитной капчи на самих сайтах).")

if __name__ == "__main__":
    print("====================================================")
    print("   РК: АВТОМАТИЗАЦИЯ СБОРА ИЗ ОТКРЫТЫХ ГОС-РЕЕСТРОВ   ")
    print("====================================================")
    
    user_input = input("Введите 12-значный ИИН (физлицо) или БИН (компания): ").strip()
    
    if len(user_input) == 12 and user_input.isdigit():
        open_legal_databases(user_input)
    else:
        print("[-] Ошибка: Номер должен состоять строго из 12 цифр.")

